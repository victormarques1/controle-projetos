    private void jBtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIncluirActionPerformed
        PessoaJuridicaController pjc = new PessoaJuridicaController();
        int erro = pjc.incluir(jTxtNomeFantasia.getText(), jTxtRazaoSocial.getText(),
                jTxtEndereco.getText(), jTxtEmail.getText(), jTxtCnpj.getText(), jTxtFone.getText());
        if(erro > -1){
            if(erro == 1062){
                JOptionPane.showMessageDialog(this, "Cnpj já cadastrado!",
                        "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.ERROR_MESSAGE);
                jTxtCnpj.requestFocus();
            }
            else JOptionPane.showMessageDialog(this, "Erro número " + erro, 
                    "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.ERROR_MESSAGE);
        }
        else{
            jBtnCancelar.setEnabled(false);
            limpaCampos(jPanelCampos);
        }
    }//GEN-LAST:event_jBtnIncluirActionPerformed

    private void jBtnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCancelarActionPerformed
        jBtnIncluir.setEnabled(true);
        jBtnCancelar.setEnabled(false);
        jBtnSalvar.setEnabled(false);
        alt = false;
        limpaCampos(jPanelCampos);
    }//GEN-LAST:event_jBtnCancelarActionPerformed

