    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
        PessoaJuridicaController pjc = new PessoaJuridicaController();
        int erro = pjc.alterar(idPessoaJuridica, jTxtNomeFantasia.getText(),jTxtRazaoSocial.getText(), jTxtEndereco.getText(), 
                jTxtCnpj.getText(), jTxtEmail.getText(), jTxtFone.getText());
        if(erro > -1){
            if(erro == 1062){
                JOptionPane.showMessageDialog(this, "CNPJ já cadastrado!!",
                        "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.ERROR_MESSAGE);
                jTxtCnpj.requestFocus();
            } 
            else JOptionPane.showMessageDialog(this, "Erro número" + erro, "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.ERROR_MESSAGE);
        }
        else{
            jBtnSalvar.setEnabled(false);
            jBtnCancelar.setEnabled(false);
            jBtnIncluir.setEnabled(true);
            alt = false;
            limpaCampos(jPanelCampos);
        }
    }//GEN-LAST:event_jBtnSalvarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        switch(JOptionPane.showConfirmDialog(this, "Confirma exclusão?", 
                "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.YES_NO_OPTION)){
            case 0: PessoaJuridicaController pjc = new PessoaJuridicaController();
            int erro = pjc.excluir(idPessoaJuridica);
            if (erro > -1)
                JOptionPane.showMessageDialog(this, "Erro número " + erro, 
                        "Controle de Projetos - Cadastro Pessoa Jurídica", JOptionPane.ERROR_MESSAGE);
            else{
                jBtnSalvar.setEnabled(false);
                jBtnCancelar.setEnabled(false);
                jBtnExcluir.setEnabled(false);
                jBtnIncluir.setEnabled(true);
                limpaCampos(jPanelCampos);
            }
            break;
            case 1: break;    
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

